package ru.gorbunovas.pcmarketplace.general

import com.crowdproj.kotlin.cor.ICorChainDsl
import com.crowdproj.kotlin.cor.handlers.worker
import ru.gorbunovas.pcmarketplace.common.PcMkplContext
import ru.gorbunovas.pcmarketplace.common.helpers.*

fun ICorChainDsl<PcMkplContext>.validateTitleHasContent(title: String) = worker {
    this.title = title
    val regExp = Regex("\\p{L}")
    on { adValidating.title.isNotEmpty() && ! adValidating.title.contains(regExp) }
    handle {
        fail(errorValidation(
            field = "title",
            violationCode = "noContent",
            description = "field must contain leters"
        ))
    }
}
